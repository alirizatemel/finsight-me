def run_scan(radar: pd.DataFrame, forecast_years: int, n_sims: int):
    records, logs = [], []
    counters = {"dÃ¶nem": 0, "fcf": 0, "piyasa": 0, "diÄŸer": 0}

    companies = radar["Åžirket"].dropna().unique()
    total = len(companies)
    progress = st.progress(0.0, text="Tarama baÅŸlÄ±yorâ€¦")

    for i, c in enumerate(companies, 1):
        try:
            row = radar[radar["Åžirket"] == c]
            bal, inc, cash = get_financials(c)

            # ðŸ”¹ ortak dÃ¶nem seÃ§imi
            periods = latest_common_period(bal, inc, cash)
            if len(periods) < 2:
                raise ValueError("ortak dÃ¶nem yok")
            curr, prev = periods[:2]

            # ðŸ”¹ temel skorlar
            f_score, _ = calculate_piotroski_f_score(row, bal, inc, curr, prev)
            m_score    = calculate_beneish_m_score(c, bal, inc, cash, curr, prev)
            graham, *_ = graham_score_card(row)
            lynch , *_ = peter_lynch_score_card(row)

            # Son 12 ay FCFâ€™i Ã§ek (yÄ±llÄ±klandÄ±rÄ±lmÄ±ÅŸ)
            df_fcf = fcf_detailed_analysis(c, row)
            if df_fcf is None or df_fcf.empty:
                raise ValueError("FCF verileri eksik.")

            # --- trailing-12-month FCF (TTM) ------------------------------------
            if len(df_fcf) >= 4:
                last_fcf = df_fcf["FCF"].iloc[-4:].sum()   # TTM: son 4 Ã§eyrek toplamÄ±
            else:
                last_fcf = df_fcf["FCF"].iloc[-1]          # fallback: tek dÃ¶nem

            if last_fcf <= 0:
                raise ValueError("Son FCF negatif veya sÄ±fÄ±r, deÄŸerleme anlamsÄ±z.")

            intrinsic = np.median(
                monte_carlo_dcf_simple(last_fcf, forecast_years=forecast_years, n_sims=n_sims)
            )
            
            # --- convert EV â†’ intrinsic value per share -------------------------------
            cur_price = row.get("Son Fiyat", pd.Series(dtype=float)).iat[0] \
                        if "Son Fiyat" in row else None

            market_cap = row.get("Piyasa DeÄŸeri", pd.Series(dtype=float)).iat[0] \
                        if "Piyasa DeÄŸeri" in row else None

            if cur_price and market_cap and market_cap > 0:
                
                shares_out = market_cap / cur_price                 # float shares
                intrinsic_ps = intrinsic / shares_out               # per-share value
                premium = (intrinsic_ps - cur_price) / cur_price

                records.append({
                    "Åžirket": c,
                    "F-Skor": f_score,
                    "M-Skor": f"{round(m_score, 2)} âš ï¸" if m_score > -2.22 else f"{round(m_score, 2)}",
                    "Graham": graham,
                    "Lynch":  lynch,
                    "Ä°Ã§sel DeÄŸer (Medyan)": intrinsic,
                    "Piyasa DeÄŸeri":        market_cap,
                    "MOS": premium,
                })

        except ValueError as exc:
            msg = str(exc).lower()
            if "dÃ¶nem" in msg:
                counters["dÃ¶nem"] += 1
            elif "fcf" in msg:
                counters["fcf"] += 1
            elif "piyasa" in msg:
                counters["piyasa"] += 1
            else:
                counters["diÄŸer"] += 1
            logs.append(f"{c}: {exc}\nâ†³ {traceback.format_exc(limit=2)}")  # <-- ek
        except Exception as exc:
            counters["diÄŸer"] += 1
            logs.append(f"{c}: {exc}")
        finally:
            progress.progress(i / total, text=f"{i}/{total} tarandÄ±â€¦")

    progress.empty()

    df = pd.DataFrame.from_records(records)
    if not df.empty:
        df["timestamp"] = datetime.now()
    if "MOS" in df.columns:
        df.sort_values("MOS", ascending=False, inplace=True)

    return df, logs, counters

